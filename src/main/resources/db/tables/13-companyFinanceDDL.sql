create table COMPANY_FINANCE  (
    FINANCE_ID	NUMBER(22) not null,
    AVERAGE_LIMIT VARCHAR2(250) not null,
    PAYMENT_TERM VARCHAR2(50) not null,
    BANK_NAME VARCHAR2(100) not null,
    ACCOUNT_NUMBER VARCHAR2(100) not null,
    CREATION_DATE	DATE,
    DISABLE_BY	VARCHAR2(20),
    DISABLE_DATE	DATE,
    LAST_UPDATED_BY	VARCHAR2(20),
    LAST_UPDATE_DATE	DATE,
    IS_DELETED VARCHAR2(1) not null,
    CURRENCY_ID NUMBER(22),
    COUNTRY_ID NUMBER(22),
    COMPANY_ID NUMBER(22),
    CUSTOMER_ID NUMBER(22),
    primary key (FINANCE_ID),
    CONSTRAINT Customer_CompanyFinance_FK FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID),
    CONSTRAINT Company_CompanyFinance_FK FOREIGN KEY (COMPANY_ID) REFERENCES COMPANY(COMPANY_ID),
    CONSTRAINT Country_CompanyFinance_FK FOREIGN KEY (COUNTRY_ID) REFERENCES COUNTRY(COUNTRY_ID),
    CONSTRAINT Currency_CompanyFinance_FK FOREIGN KEY (CURRENCY_ID) REFERENCES CURRENCY(CURRENCY_ID)
)