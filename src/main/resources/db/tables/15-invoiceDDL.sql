create table INVOICE  (
      INVOICE_ID	NUMBER(22) not null,
      RE VARCHAR2(100) not null,
      CASE_REMARK VARCHAR2(50) not null,
      REJECT_REMARK VARCHAR2(100) not null,
      INVOICE_TITLE VARCHAR2(100) not null,
      VAT VARCHAR2(100) not null,
      EXCHANGE_RATE DECIMAL(9,2) not null,
      PAYMENT_TERM VARCHAR2(100) not null,
      INVOICE_NUMBER VARCHAR2(100) not null,
      BANK_ACCOUNT VARCHAR2(100) not null,
      DESCRIPTION VARCHAR2(1000) not null,
      STATUS VARCHAR2(100),
      INVOICE_DATE Date not null,
      CREATION_DATE	DATE,
      DISABLE_BY	VARCHAR2(20),
      DISABLE_DATE	DATE,
      LAST_UPDATED_BY	VARCHAR2(20),
      LAST_UPDATE_DATE	DATE,
      IS_DELETED VARCHAR2(1) not null,
      CASE_ID NUMBER(22),
      CURRENCY_ID NUMBER(22),
      CUSTOMER_ID NUMBER(22),
      primary key (INVOICE_ID),
      CONSTRAINT Customer_Invoice_FK FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID),
      CONSTRAINT Currency_Invoice_FK FOREIGN KEY (CURRENCY_ID) REFERENCES CURRENCY(CURRENCY_ID)
)