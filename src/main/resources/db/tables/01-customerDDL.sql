create table CUSTOMER  (
    CUSTOMER_ID	NUMBER(22),
    FIRST_NAME VARCHAR2(250) ,
    LAST_NAME VARCHAR2(250),
    COMPANY_NAME VARCHAR2(250),
    BUSINESS_TYPE VARCHAR2(50) ,
    TAX_ID VARCHAR2(100) ,
    WEBSITE VARCHAR2(100),
    TRADING_RELATION VARCHAR2(50),
    GENDER VARCHAR2(50),
    "TYPE" VARCHAR2(50) not null,
    DOB DATE,
    ID_PASSPORT_NO VARCHAR2(50),
    EMAIL VARCHAR2(50),
    CONTACT_NO VARCHAR2(50),
    code NUMBER(22),
    REGISTER_DATE DATE not null,
    CREATED_BY	VARCHAR2(20),
    CREATION_DATE	DATE,
    DISABLE_BY	VARCHAR2(20),
    DISABLE_DATE	DATE,
    LAST_UPDATED_BY	VARCHAR2(20),
    LAST_UPDATE_DATE	DATE,
    IS_DELETED VARCHAR2(1) not null,
    primary key (CUSTOMER_ID),
    CONSTRAINT gender_enum CHECK (GENDER IN ('Male', 'Female', 'Other'))
)
