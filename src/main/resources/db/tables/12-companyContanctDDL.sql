create table COMPANY_CONTACT_PERSON  (
      CONTACT_PERSON_ID	NUMBER(22) not null,
      CONTACT_NAME VARCHAR2(250) not null,
      DEP VARCHAR2(50) not null,
      TITLE VARCHAR2(100) not null,
      PHONE VARCHAR2(100) not null,
      EMAIL VARCHAR2(50) not null,
      DESCRIPTION	VARCHAR2(500),
      CREATION_DATE	DATE,
      DISABLE_BY	VARCHAR2(20),
      DISABLE_DATE	DATE,
      LAST_UPDATED_BY	VARCHAR2(20),
      LAST_UPDATE_DATE	DATE,
      IS_DELETED VARCHAR2(1) not null,
      SITE_ID NUMBER(22),
      CUSTOMER_ID NUMBER(22),
      primary key (CONTACT_PERSON_ID),
      CONSTRAINT Customer_ComContact_FK FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID),
      CONSTRAINT Site_ComContact_FK FOREIGN KEY (SITE_ID) REFERENCES SITE(SITE_ID)
)