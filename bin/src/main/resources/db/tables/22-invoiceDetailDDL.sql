create table INVOICE_DETAIL  (
       INVOICE_DETAIL_ID	NUMBER(22) not null,
       INVOICE_ID	NUMBER(22) not null,
       ITEM_ID	NUMBER(22),
       ITEM_AMOUNT DECIMAL(9,2),
       INSTALLMENT_ID	NUMBER(22),
       INSTALLMENT_AMOUNT DECIMAL(9,2),
       DEPOSIT_ID	NUMBER(22),
       DEPOSIT_AMOUNT DECIMAL(9,2),
       CREATION_DATE	DATE,
       DISABLE_BY	VARCHAR2(20),
       DISABLE_DATE	DATE,
       LAST_UPDATED_BY	VARCHAR2(20),
       LAST_UPDATE_DATE	DATE,
       IS_DELETED VARCHAR2(1) not null,
       primary key (INVOICE_DETAIL_ID),
       CONSTRAINT Invoice_InvoiceDetail_FK FOREIGN KEY (INVOICE_ID) REFERENCES INVOICE(INVOICE_ID),
       CONSTRAINT Item_InvoiceDetail_FK FOREIGN KEY (ITEM_ID) REFERENCES ITEM(ITEM_ID),
       CONSTRAINT Installment_InvoiceDetail_FK FOREIGN KEY (INSTALLMENT_ID) REFERENCES INSTALLMENT(INSTALLMENT_ID),
       CONSTRAINT Deposit_InvoiceDetail_FK FOREIGN KEY (DEPOSIT_ID) REFERENCES DEPOSIT(DEPOSIT_ID)
)